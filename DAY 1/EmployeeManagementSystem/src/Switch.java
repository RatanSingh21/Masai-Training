public class Switch {

    public static void main(String[] args) {
         int i = 10;

         switch (i){
             case 5:
                 System.out.println("FIve");
                 break;
             case 7:
                 System.out.println("seven");
                 break;
             case 10:
                 System.out.println("Ten");
                 break;
             case 15:
                 System.out.println("Fifteen");
                 break;
             default:
                 System.out.println("Default");
//                 break;
         }
    }
}
